    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
       String nCliente = nomCliente.getText();
       String nEncargado = nomEncargado.getText();
       String telf = telefonoTxt.getText();
       String mail = correoTxt.getText();
       
       // Validaciones para los campos
       if (nCliente.isEmpty()|| nEncargado.isEmpty() || telf.isEmpty() || mail.isEmpty() ){
          javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
          nomCliente.requestFocus();
          return;
       } 
       // Validacion de numero telefonico
        if (telefonoTxt.getText().length()!=9) {
            javax.swing.JOptionPane.showMessageDialog(this, "Numero incorrecto, debe ingresar 9 digitos.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        } 
       //validacion de correo
       if(!correoTxt.getText().contains("@")||!correoTxt.getText().contains(".")){
           javax.swing.JOptionPane.showMessageDialog(this, "Correo no valido.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
       }
       com.mycompany.mecatech.models.clientes cliente = isEdition ? clienteEdition : new com.mycompany.mecatech.models.clientes();
       cliente.setNom_cliente(nCliente);
       cliente.setEncargado(nEncargado);
       cliente.setTelefono(telf);
       cliente.setCorreo(mail);
       
        try {
            DAOclientes dao = new DAOclientesImpl();
            
            if (!isEdition) {
                dao.registrar(cliente);
            }else{
                dao.modificar(cliente);
            }
            
            String successMsg = isEdition ? "modificado" : "registrado";
            javax.swing.JOptionPane.showMessageDialog(this,"Cliente " + successMsg + " exitosamente. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE );
            if (!isEdition) {
               nomCliente.setText("");
               nomEncargado.setText("");
               telefonoTxt.setText("");
               correoTxt.setText("");
            }
        } catch (Exception e) {
            String errorMsg = isEdition ? "modificar" : "registrar";
            javax.swing.JOptionPane.showMessageDialog(this, "OcurriÃ³ un error al " + errorMsg + " el cliente.  \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_buttonActionPerformed

